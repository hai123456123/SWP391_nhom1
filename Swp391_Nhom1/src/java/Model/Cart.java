package Model;

/**
 *
 * @author ADMIN
 */
public class Cart {
    private int id;
    private int userId;
    private int pid;
    private int quantity;
    private long price;
    private String image;
    private String name;

    public Cart() {
    }

    public Cart(int id, int userId, int pid, int quantity, long price, String image, String name) {
        this.id = id;
        this.userId = userId;
        this.pid = pid;
        this.quantity = quantity;
        this.price = price;
        this.image = image;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public long getPrice() {
        return price;
    }

    public void setPrice(long price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

     
}
